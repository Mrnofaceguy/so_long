#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		start;
	int		end;
	int		i;
	char	*out;

	start = 0;
	i = 0;
	if (!s1 || !set)
		return (NULL);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1) - 1;
	while (end >= start && ft_strchr(set, s1[end]))
		end--;
	out = ft_calloc(end - start + 2, sizeof(char));
	if (!out)
		return (NULL);
	while (start <= end)
	{
		out[i++] = s1[start++];
	}
	out[i] = '\0';
	return (out);
}
